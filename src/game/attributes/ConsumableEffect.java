package game.attributes;

/**
 * Enum representing the different effects that consumable items can have on an actor.
 * This enum is used to define the various effects that consumable items can have when used by an actor.
 * Each effect corresponds to a specific action or change in the actor's attributes.
 * 
 * @author Tadiwa
 */
public enum ConsumableEffect {
    HEAL,
    MAX_HEALTH_INCREASE,
    STAMINA_RESTORE,
    RUNES_ADDED
}
